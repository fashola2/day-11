let formDatalist = document.querySelector("#fields")

for( let i = 0; i < formData.length; i++ ){

let form = formData[i];


if(form.type == "textarea") {
  let textarea = document.createElement("textarea")
  textarea.placeholder = form.label
  formDatalist.append(textarea)
} else if (form.type == "select") {
    let select = document.createElement("select")
    formDatalist.append(select)
    for (var e = 0; e < form.options.length; e++) {
      let option = document.createElement("option")
       option.label = form.options[e].label;
      select.add(option)
    }
  } else {
    let textBox = document.createElement("input")
    textBox.type = form.type;
    textBox.placeholder = form.label;
    formDatalist.append(textBox)
  }
}
